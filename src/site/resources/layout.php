            <h2>Version 2.5.0 released</h2><p>We are proud to announce version <a href="/download/release/2.5.0/changelog.html">2.5.0</a> of PDepend. For more details visit the version release <a href="/download/release/2.5.0/changelog.html">site</a>.</p><p>The prefered way to install PDepend is as a <a href="https://web.archive.orghttps://static.pdepend.org/php/latest/pdepend.phar">phar</a> (<a href="https://web.archive.orghttps://static.pdepend.org/php/latest/pdepend.bz2.phar">bz2</a>) archive:</p><code class="block shell">~ $ wget http://static.pdepend.org/php/latest/pdepend.phar</code><p>or you can use <a href="https://web.archive.orghttp://getcomposer.org/">composer</a> by just adding PHP_Depend as a dependency to your <code class="inline">composer.json</code></p><code class="block">{
                "require": {

                "pdepend/pdepend" : "2.5.0"
                }
                }</code><p>Then install Composer in your project (or <a href="https://web.archive.orghttp://getcomposer.org/composer.phar">download the composer.phar</a> directly):</p><code class="block shell">~ $ curl -s http://getcomposer.org/installer | php</code><p>And finally let Composer install the project dependencies:</p><code class="block shell">~ $ php composer.phar install</code><a name="download"></a><h3>Download</h3><p>Download the latest version of PHP_Depend as a <a href="https://web.archive.orghttps://static.pdepend.org/php/2.5.0/pdepend.phar">Phar archive</a> (<a href="https://web.archive.orghttps://static.pdepend.org/php/2.5.0/pdepend.bz2.phar">bz2</a>) or <a href="https://web.archive.orghttp://packagist.org/packages/pdepend/pdepend">Composer</a> dependency.</p>


